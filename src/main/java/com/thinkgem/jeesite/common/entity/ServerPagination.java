package com.thinkgem.jeesite.common.entity;

import java.util.List;

public class ServerPagination<T> implements java.io.Serializable {

	private static final long serialVersionUID = 1L;
	
	private int total;
	private List<T> rows;
	private List<T> errorrows;
	private List<T> successrows;
	private List<String> eventTypeDicList;
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public List<T> getRows() {
		return rows;
	}
	public void setRows(List<T> rows) {
		this.rows = rows;
	}
	public List<String> getEventTypeDicList() {
		return eventTypeDicList;
	}
	public void setEventTypeDicList(List<String> eventTypeDicList) {
		this.eventTypeDicList = eventTypeDicList;
	}
	public List<T> getErrorrows() {
		return errorrows;
	}
	public void setErrorrows(List<T> errorrows) {
		this.errorrows = errorrows;
	}
	public List<T> getSuccessrows() {
		return successrows;
	}
	public void setSuccessrows(List<T> successrows) {
		this.successrows = successrows;
	}
	
	
	

}
